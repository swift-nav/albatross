TEST(test_minimum_spanning_tree, test_princeton_tiny) {

  std::vector<Edge<std::size_t>> edges;
  edges.emplace_back(4, 5, 0.35);
  edges.emplace_back(4, 7, 0.37);
  edges.emplace_back(5, 7, 0.28);
  edges.emplace_back(0, 7, 0.16);
  edges.emplace_back(1, 5, 0.32);
  edges.emplace_back(0, 4, 0.38);
  edges.emplace_back(2, 3, 0.17);
  edges.emplace_back(1, 7, 0.19);
  edges.emplace_back(0, 2, 0.26);
  edges.emplace_back(1, 2, 0.36);
  edges.emplace_back(1, 3, 0.29);
  edges.emplace_back(2, 7, 0.34);
  edges.emplace_back(6, 2, 0.40);
  edges.emplace_back(3, 6, 0.52);
  edges.emplace_back(6, 0, 0.58);
  edges.emplace_back(6, 4, 0.93);

  const auto tree = minimum_spanning_tree(create_graph(edges));

  double cost = 0.;
  for (const auto &edge : tree.edges) {
    cost += edge.cost;
  }
  EXPECT_EQ(cost, 1.81);
}

